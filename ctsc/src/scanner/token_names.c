const char* ctsc_syntax_kind_name(CtscSyntaxKind k) {
    if ((unsigned)k >= (unsigned)CTSC_SK__COUNT) { return "Unknown"; }
    const char* n = k_names[k];
    return n ? n : "Unknown";
}

