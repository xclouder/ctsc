CtscSyntaxKind ctsc_identifier_or_keyword(const uint16_t* text, size_t len) {
    if (len == 0 || len > 16) { return CTSC_SK_Identifier; }
    for (size_t i = 0; i < sizeof(k_keywords) / sizeof(k_keywords[0]); ++i) {
        const Keyword* k = &k_keywords[i];
        if (k->len != len) { continue; }
        bool match = true;
        for (size_t j = 0; j < len; ++j) {
            if ((uint16_t)k->name[j] != text[j]) { match = false; break; }
        }
        if (match) { return k->kind; }
    }
    return CTSC_SK_Identifier;
}
